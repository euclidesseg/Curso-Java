package Clases;

public class Clase1 {
    /*  Los modificadores de acceso en Java son palabras clave que controlan la
        visibilidad y el alcance de clases, métodos y atributos.
        Son:

        public → Accesible desde cualquier parte del programa.
        private → Solo accesible dentro de la misma clase.
        protected → Accesible dentro del mismo paquete y por clases hijas (herencia).
        (default) (sin modificador) → Accesible solo dentro del mismo paquete.
    */
    public int atributoUno;
    private int atributoDos;
    protected  int atributoTres;
    int atributoCuatro;

}
