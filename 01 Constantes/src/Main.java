public class Main {
    public static void main(String[] args) {
        // En java existen dos tipo de datos constantes y variables,
        // las constantes se declaran con la palabra reservada final, las constantes son un tipo de datos que no se pueden
        // modificar durante toda la vida del programa

        final int numero = 5;
        //numero = 2; no se podrá realizar esta acción ya que numero es constante
        System.out.println(numero);
    }
}