package Clases;

public class Estatico {
    /*
    *   Los miembros estáticos de una clase en Java son los métodos y
    *   propiedades que pertenecen a la clase,
    *   no a cada objeto que se cree a partir de ella
    * */

    public static String frase = "Primera frase";

    public static int sumar(int numero1, int numero2){
        return numero1 + numero2;
    }


}
